package operators;

public class ArithmeticOperators {
    public static void main(String[] args){
        int a = 25;
        int b = 12;

       System.out.println("a + b");  //(" ") -- it stores String.
       System.out.println(a+b);  //( ) -- it stores operations.
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
    }
}
